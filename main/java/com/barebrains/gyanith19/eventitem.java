package com.barebrains.gyanith19;

public class eventitem {

    String name,time;

    public eventitem() {
    }

    public eventitem(String name, String time) {
        this.name = name;
        this.time = time;
    }

    public String getName() {
        return name;
    }

    public String getTime() {
        return time;
    }
}
